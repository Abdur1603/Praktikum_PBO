    private void btnLihatCusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLihatCusActionPerformed
            dataCustomer dataForm = new dataCustomer();
        
            StringBuilder sb = new StringBuilder();
            if (daftarCustomer.isEmpty()) {
                sb.append("Belum ada data customer.");
            } else {
                for (Customer c : daftarCustomer) {
                    sb.append("ID Customer: ").append(c.getId()).append("\n");
                    sb.append("Nama: ").append(c.getNama()).append("\n");
                    sb.append("Alamat: ").append(c.getAlamat()).append("\n\n");
                }
            }
            dataForm.setOutput(sb.toString());

            dataForm.setVisible(true);
            dispose();
    }//GEN-LAST:event_btnLihatCusActionPerformed

