    private void btnLihatPesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLihatPesanActionPerformed
        dataCustomer pesananForm = new dataCustomer();

        // Buat isi daftar pesanan
        StringBuilder sb = new StringBuilder();
        if (daftarPesanan.isEmpty()) {
            sb.append("Belum ada pesanan.");
        } else {
            sb.append("=== DAFTAR PESANAN ===\n\n");
            for (Pesanan p : daftarPesanan) {
                sb.append("ID Customer : ").append(p.getIdCustomer()).append("\n");
                sb.append("Nama        : ").append(p.getNamaCustomer()).append("\n");
                sb.append("ID Tabung   : ").append(p.getIdTabung()).append("\n");
                sb.append("Jenis Gas   : ").append(p.getJenisTabung()).append("\n");
                sb.append("Jumlah      : ").append(p.getJumlahPesan()).append("\n\n");
            }
        }

        pesananForm.setOutput(sb.toString());
        pesananForm.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnLihatPesanActionPerformed

